public class Operator {
    public static void main(String[] args) {
        int hasil = 1 + 2;
        System.out.println("1+2=" + hasil);

        hasil = hasil - 1;
        System.out.println("hasilnya sekarang " + hasil);

        hasil = hasil * 2;
        System.out.println(hasil);

        hasil = hasil / 2;
        System.out.println(hasil);

        hasil += 10;
        System.out.println(hasil);

        hasil -= 10;
        System.out.println(hasil);

        hasil *= 20;
        System.out.println(hasil);
    }
}
